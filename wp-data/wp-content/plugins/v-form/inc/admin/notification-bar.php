  const NOTIF_KEY = 'vformNotifClosedUntil_2';

  function hideNotification() {
    const oneWeekFromNow = Date.now() + 7 * 24 * 60 * 60 * 1000;
    localStorage.setItem(NOTIF_KEY, oneWeekFromNow);
    document.getElementById('notifBar').style.display = 'none';
  }

  function shouldShowNotification() {
    const hideUntil = localStorage.getItem(NOTIF_KEY);
    return !hideUntil || Date.now() > parseInt(hideUntil, 10);
  }

  window.onload = () => {
    if (!shouldShowNotification()) {
      document.getElementById('notifBar').style.display = 'none';
